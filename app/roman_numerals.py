def parse(rn):
    if rn == "I":
        return 1
    elif rn == "II":
        return 2
    elif rn == "III":
        return 3
    elif rn == "IV":
        return 4
    elif rn == "V":
        return 5
    elif rn == "VI":
        return 6
    elif rn == "VII":
        return 7
    elif rn == "VIII":
        return 8
